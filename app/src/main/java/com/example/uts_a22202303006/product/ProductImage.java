package com.example.uts_a22202303006.product;

public class ProductImage {
    private int id;
    private String product_code;
    private String foto;
    private int image_order;

    // Getters and setters
    public int getId() { return id; }
    public void setId(int id) { this.id = id; }

    public String getProduct_code() { return product_code; }
    public void setProduct_code(String product_code) { this.product_code = product_code; }

    public String getFoto() { return foto; }
    public void setFoto(String foto) { this.foto = foto; }

    public int getImage_order() { return image_order; }
    public void setImage_order(int image_order) { this.image_order = image_order; }
}
